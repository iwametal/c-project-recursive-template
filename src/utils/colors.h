#ifndef _COLORS_H_
#define _COLORS_H_

/**
 *
 * ANSI color definitions for pretty-print
 *
 */

#define NO_COLOR    "\x1b[0m"
#define RED         "\x1b[31m"
#define GREEN       "\x1b[32m"
#define BROWN       "\x1b[33m"
#define BLUE        "\x1b[34m"
#define MAGENTA     "\x1b[35m"
#define CYAN        "\x1b[36m"
#define GRAY        "\x1b[37m"

#endif // COLORS_H
